package impressoes;

//19)	Imprimir o resultado do número elevado ao quadrado, de uma lista com 100 posições.
public class numero19 {
	public static void main(String[] args) {
		for (int a = 0; a <= 100; a++) {
			int expoente = a * a;
			{
				System.out.println(expoente);
			}
		}

	}
}